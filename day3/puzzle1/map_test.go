package main

import "testing"

func TestTresInMap(t *testing.T) {
	testCases := []struct {
		name string
		area [][]rune
		want int
	}{
		{
			name: "Example",
			area: [][]rune{
				{'.', '.', '#', '#', '.', '.', '.', '.', '.', '.', '.'},
				{'#', '.', '.', '.', '#', '.', '.', '.', '#', '.', '.'},
				{'.', '#', '.', '.', '.', '.', '#', '.', '.', '#', '.'},
				{'.', '.', '#', '.', '#', '.', '.', '.', '#', '.', '#'},
				{'.', '#', '.', '.', '.', '#', '#', '.', '.', '#', '.'},
				{'.', '.', '#', '.', '#', '#', '.', '.', '.', '.', '.'},
				{'.', '#', '.', '#', '.', '#', '.', '.', '.', '.', '#'},
				{'.', '#', '.', '.', '.', '.', '.', '.', '.', '.', '#'},
				{'#', '.', '#', '#', '.', '.', '.', '#', '.', '.', '.'},
				{'#', '.', '.', '.', '#', '#', '.', '.', '.', '.', '#'},
				{'.', '#', '.', '.', '#', '.', '.', '.', '#', '.', '#'},
			},
			want: 7,
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			got := TreesInMap(tc.area)

			if got != tc.want {
				t.Errorf("TreesInMap() = got %d; want %d.", got, tc.want)
			}
		})
	}
}
